# contagem normal
for c1 in range(0,7):
    print(c1)
print('Fim do primeiro teste.')

# contagem regressiva
for c2 in range(7, 0, -1):
    print(c2)
print('Fim do segundo teste.')

# contagem de 0 a 7, pulando de 2 em 2
for c3 in range(0, 7, 2):
    print(c3)
print('Fim do terceiro teste')

