nome = str(input('Qual é seu nome? ')).upper()

if nome == 'YURI':
    print(f'Nome zica hein {nome}')
elif nome == 'YGOR' or nome == 'GODOFREDO' or nome == "JAIME":
    print(f'{nome} é nome de mordomo de filme de terror.')
elif nome == 'JUREMA' or nome == 'RAIMUNDA' or nome == 'MARIA' or nome == 'LUISA' or nome == 'RENATA':
    print(f'{nome}... Mas que belo nome minha filha. ')
else:
    print(f'Pode crê! Nõ gostei muito de {nome} não.')
print(f'Tenha um bom dia {nome}.')